    private void ConBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConBtActionPerformed

        String mdpEntered = jPasswordField.getText();
        String idEntered = jTextField_identifiant.getText();
        String q = "SELECT * FROM personnel WHERE (Identifiant = '" + idEntered + "')";

        DbConnection c = new DbConnection();
        boolean b = c.connexionB();
        // Si la connexion à la BD est bien assurée ... 
        if (b) {
            try {
                // On retourne les infos personnel de celui qui s'identifie 
                ResultSet rs = c.select(q);
                while (rs.next()) {
                    String nom = rs.getString("Nom");
                    String prenom = rs.getString("Prenom");
                    String token = rs.getString("Token");
                    String spe = rs.getString("Spécialité");

                    boolean auth = cr.authenticate(mdpEntered.toCharArray(), token);

                    // On vérifie que le personnel qui s'identifie existe dans la BD
                    if (rs != null) {
                        // Authentification du MDP crypté dans la BDD
                        if (auth) {
                            // Ouverture d'une nouvelle fenêtre d'accueil en fonction du métier
                            switch (spe) {
                                case "MEDECIN":
                                    // On rempli l'objet Personnel du personnel qui se connecte
                                    p.setNom(nom);
                                    p.setPrenom(prenom);
                                    Medecin_Accueil aM = new Medecin_Accueil();
                                    // On lance la fenêtre Accueil du personnel qui se connecte
                                    this.setVisible(false);
                                    aM.setVisible(true);
                                    break;
                                case "SECRET":
                                    p.setNom(nom);
                                    p.setPrenom(prenom);
                                    Secretaire_Accueil aS = new Secretaire_Accueil();
                                    this.setVisible(false);
                                    aS.setVisible(true);
                                    break;
                                case "INTERN":
                                    p.setNom(nom);
                                    p.setPrenom(prenom);
                                    this.setVisible(false);
                                    Manipulateur_Accueil aI = new Manipulateur_Accueil();
                                    aI.setVisible(true);
                                    break;
                                case "MANIP":
                                    p.setNom(nom);
                                    p.setPrenom(prenom);
                                    this.setVisible(false);
                                    Manipulateur_Accueil aMP = new Manipulateur_Accueil();
                                    aMP.setVisible(true);
                                    break;
                                case "DECO":
                                    break;
                                default:
                                    throw new AssertionError();
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Connexion impossible : \t Mot de passe éronné \n\t\tVeuillez réessayer");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Connexion impossible : \t Identifiant éronné \n\t\tVeuillez réessayer");
                    }
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                ex.getSQLState();
                ex.getCause();
                ex.getErrorCode();
                JOptionPane.showMessageDialog(this, "Votre identifiant et/ou votre mot de passe sont erronés \n Veuillez réessayer", "Erreur", JOptionPane.WARNING_MESSAGE);
            } finally {
                c.close();
            }
        }
    }//GEN-LAST:event_ConBtActionPerformed

